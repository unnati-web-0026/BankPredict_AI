cat_cols = ['job', 'marital', 'education', 'default',
            'housing', 'loan', 'contact', 'month', 'poutcome']

def predict_customer(customer_dict):
    # Convert to dataframe
    sample = pd.DataFrame([customer_dict])

    # One-hot encode using same columns as training
    sample_enc = pd.get_dummies(sample, columns=cat_cols, drop_first=True)

    # Align with training feature set (add missing cols as 0)
    sample_enc = sample_enc.reindex(columns=X.columns, fill_value=0)

    # Scale
    sample_sc = scaler.transform(sample_enc)

    # Predict
    pred = rf.predict(sample_sc)[0]
    proba = rf.predict_proba(sample_sc)[0][1]

    label = 'YES ✅' if pred == 1 else 'NO ❌'
    print(f'Prediction : {label}')
    print(f'Probability: {proba:.2%}')
    return pred, proba

