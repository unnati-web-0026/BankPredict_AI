def cap_outliers(df, balance):
    Q1 = df[balance].quantile(0.25)
    Q3 = df[balance].quantile(0.75)
    IQR = Q3 - Q1
    lower = Q1 - 1.5 * IQR
    upper = Q3 + 1.5 * IQR
    df[balance] = df[balance].clip(lower, upper)
    return df

